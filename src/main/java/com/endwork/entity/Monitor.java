package com.endwork.entity;

public class Monitor {
    private int monitor_id;
    private String monitor_pwd;
    private String monitor_name;
    private String monitor_sex;
    private String monitor_phone;

    public int getMonitor_id() {
        return monitor_id;
    }

    public void setMonitor_id(int monitor_id) {
        this.monitor_id = monitor_id;
    }

    public String getMonitor_pwd() {
        return monitor_pwd;
    }

    public void setMonitor_pwd(String monitor_pwd) {
        this.monitor_pwd = monitor_pwd;
    }

    public String getMonitor_name() {
        return monitor_name;
    }

    public void setMonitor_name(String monitor_name) {
        this.monitor_name = monitor_name;
    }

    public String getMonitor_sex() {
        return monitor_sex;
    }

    public void setMonitor_sex(String monitor_sex) {
        this.monitor_sex = monitor_sex;
    }

    public String getMonitor_phone() {
        return monitor_phone;
    }

    public void setMonitor_phone(String monitor_phone) {
        this.monitor_phone = monitor_phone;
    }
}
